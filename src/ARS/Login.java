    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
    String userl = mail.getText();
    String passlg =String.valueOf(passl.getPassword()) ;
    try{
    DB con = new DB();
    String q ="Select * From userdb where username='"+userl+"'and password='"+passlg+"'";
    ResultSet rs=con.stm.executeQuery(q);
    if(rs.next()){
        UserPanel.em = userl;
        UserPanel home = new UserPanel();
        home.setVisible(true);
        home.pack();
        home.setLocationRelativeTo(null);
        this.dispose();
        }
    else{
    JOptionPane.showMessageDialog(null,"Wrong User name or Password!");
       this.setVisible(false);
       this.setVisible(true);
    }
    }
    catch(Exception e){
        e.printStackTrace();
    }
    }//GEN-LAST:event_loginActionPerformed

